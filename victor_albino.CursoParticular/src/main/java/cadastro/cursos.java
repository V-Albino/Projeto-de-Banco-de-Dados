    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        curso inserirCurso = new curso();
        inserirCurso.setNome(txtNome.getText());
        inserirCurso.setEndereco(txtEndereco.getText());
        inserirCurso.setSalas(txtSalas.getHeight());
        inserirCurso.setPreco(txtPreco.getText());
        cursoDao ad = new cursoDao();
        ad.inserirCurso(inserirCurso);
    }//GEN-LAST:event_jButton1ActionPerformed

